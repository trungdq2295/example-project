package sec03;

public class NumberSystems {

    public static void main(String[] args) {

        int dec = 152;
        int bin = 0b10011000;
        int oct = 0230;
        int hex = 0x98;

        // Will print these to decimal format
        System.out.println(dec);
        System.out.println(bin);
        System.out.println(oct);
        System.out.println(hex);
    }
}
